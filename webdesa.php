//Efek Smooth Scroll
$('a[href^="#"]').on('click'
,function (e) {
     e.preventDefault();

     var target = this.hash,
        $target = $(target);

     $('html, body').animate({'scrollTop': $target.offset().top
   }, 2000, 'swing', function () {window.location.hash = target;}); });

// paralax
   $(window).scroll(function(){
     var scrol = $(this).scrollTop();

     $('.jumbotron img').css({
       'transform' : 'translate(0px,'+ scrol/5 +'%)'
     });

     $('.jumbotron h1').css({
       'transform' : 'translate(0px,'+ scrol/1 +'%)'
     });

     $('.jumbotron p').css({
       'transform' : 'translate(0px,'+ scrol/6 +'%)'
     });

   });


// navbar fix ketika scroll
$(function(){

	var menu = $('#twd-menu'),
		pos = menu.offset();

		$(window).scroll(function(){
			if($(this).scrollTop() > pos.top+menu.height() && menu.hasClass('normal')){
				menu.fadeOut('slow', function(){
					$(this).removeClass('normal').addClass('fixed').fadeIn('slow');
				});
			} else if($(this).scrollTop() <= pos.top && menu.hasClass('fixed')){
				menu.fadeOut('slow', function(){
					$(this).removeClass('fixed').addClass('normal').fadeIn('normal');
				});
			}
		});

});

</script>
